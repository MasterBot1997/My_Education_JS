package Task03;

public class calcOp {
    static int sum(int a, int b) {
        int c = a + b;
        return c;
    }

    static int dif(int a, int b) {
        int c = a - b;
        return c;
    }

    static int mult(int a, int b) {
        int c = a * b;
        return c;
    }

    static int div(int a, int k) {
        int c = a / k;
        return c;
    }
}
